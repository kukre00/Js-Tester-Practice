//Using var, let, and const variable

/*Var Variable
var userName = "Kukre00"
console.log(`The username is ${userName}`)

Update
userName = "Kuks"
console.log(`The new username is ${userName}`)
*/


/*Let Variable
let petName = "Hoshi"
console.log(`My pet's name, ${petName}`)

//Update
petName = "Luna"
console.log(`My pet's name, ${petName}`)
*/

//The var and let are similar, they can update or reassign a data. The let variable is new version of var variable

//Const Variable
const userId = "09-10-0001"
console.log(`User ID: ${userId}`)

//in const variable, the data you put are not updatable or it cannot reassign a data. 

